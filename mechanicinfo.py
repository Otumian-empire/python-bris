class mechanicInfo:
    def __init__(self, mName, mSalary):
        self.name = mName
        self.salary = mSalary

    def getName(self):
        return self.name

    def getSalary(self):
        return self.salary

    def setName(self, mName):
        self.name = mName

    def setSalary(self, mSalary):
        self.salary = mSalary
